function createHoles(config) {
  const holes = [];

  for (let y = 62; y <= 226; y += 24) {
    for (let x = 108; x <= 252; x += 24) {
      const dx = (x - 180) / config.radiusX;
      const dy = (y - config.centerY) / config.radiusY;

      if (dx * dx + dy * dy < 0.72) {
        holes.push(`<circle class="hole" cx="${x}" cy="${y}" r="4.6"/>`);
      }
    }
  }

  return holes.join("");
}

